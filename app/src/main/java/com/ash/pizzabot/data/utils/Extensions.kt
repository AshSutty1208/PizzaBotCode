package com.ash.pizzabot.data.utils

/**
 * Checks if an incoming Int is lower than the current int
 *
 * @param intToCompare - Int to check is is lower
 */
fun Int.isLowerThan(intToCompare: Int) = this < intToCompare

/**
 * Checks if an incoming Int is greater than the current int
 *
 * @param intToCompare - Int to check is is greater
 */
fun Int.isGreaterThan(intToCompare: Int) = this > intToCompare